def plot_score_distributions(per_image: List[Dict], save_path: Path,
                              baseline_data: Optional[List[Dict]] = None):
    """
    Plot score distributions for each available metric.
    If baseline_data is provided, overlay trained vs baseline.
    """
    metrics_to_plot = ["clip_score", "vqa_score", "dsg_score",
                       "t2i_object", "t2i_attribute", "t2i_relation"]
    labels = {
        "clip_score":    "CLIPScore",
        "vqa_score":     "VQAScore",
        "dsg_score":     "DSGScore",
        "t2i_object":    "T2I Object",
        "t2i_attribute": "T2I Attribute",
        "t2i_relation":  "T2I Relation",
    }

    available = [m for m in metrics_to_plot
                 if any(m in d for d in per_image)]
    if not available:
        return

    fig, axes = plt.subplots(1, len(available),
                              figsize=(4 * len(available), 4))
    if len(available) == 1:
        axes = [axes]

    for ax, metric in zip(axes, available):
        scores = [d[metric] for d in per_image if metric in d]
        ax.hist(scores, bins=15, alpha=0.7, label="Trained", color="steelblue")

        if baseline_data:
            base_scores = [d[metric] for d in baseline_data if metric in d]
            if base_scores:
                ax.hist(base_scores, bins=15, alpha=0.7,
                        label="Baseline", color="coral")

        ax.set_title(labels.get(metric, metric))
        ax.set_xlabel("Score")
        ax.set_ylabel("Count")
        ax.axvline(np.mean(scores), color="navy", linestyle="--",
                   linewidth=1.5, label=f"Mean={np.mean(scores):.3f}")
        if baseline_data:
            base_scores = [d[metric] for d in baseline_data if metric in d]
            if base_scores:
                ax.axvline(np.mean(base_scores), color="darkred",
                           linestyle="--", linewidth=1.5,
                           label=f"Base={np.mean(base_scores):.3f}")
        ax.legend(fontsize=7)

    plt.suptitle("Score Distributions", fontsize=12)
    plt.tight_layout()
    plt.savefig(save_path, dpi=100, bbox_inches="tight")
    plt.close(fig)
    print(f"Saved score distributions: {save_path}")


def print_summary(aggregate: Dict, baseline_agg: Optional[Dict] = None):
    """Print a formatted summary table."""
    metrics = [
        ("CLIPScore",     "clip_score"),
        ("VQAScore",      "vqa_score"),
        ("DSGScore",      "dsg_score"),
        ("T2I Object",    "t2i_object"),
        ("T2I Attribute", "t2i_attribute"),
        ("T2I Relation",  "t2i_relation"),
        ("T2I Overall",   "t2i_overall"),
    ]
    print(f"\n{'─'*60}")
    print(f"{'Metric':<18} {'Trained':>10}  {'Baseline':>10}  {'Delta':>8}")
    print(f"{'─'*60}")
    for label, key in metrics:
        if key not in aggregate:
            continue
        val  = aggregate[key]["mean"]
        bval = (baseline_agg or {}).get(key, {}).get("mean")
        delta_str = ""
        if bval is not None:
            delta = val - bval
            sign  = "+" if delta >= 0 else ""
            delta_str = f"{sign}{delta:+.4f}"
        bval_str = f"{bval:.4f}" if bval is not None else "   —"
        print(f"{label:<18} {val:>10.4f}  {bval_str:>10}  {delta_str:>8}")
    print(f"{'─'*60}")
    n = aggregate.get("n_images", "?")
    print(f"N images: {n}")
    print()



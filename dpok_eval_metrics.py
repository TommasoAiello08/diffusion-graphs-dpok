def main():
    args   = parse_args()
    device = (
        "cuda" if args.device == "auto" and torch.cuda.is_available()
        else args.device if args.device != "auto"
        else "cpu"
    )
    print(f"Device: {device}")

    save_dir = Path(args.save_dir)
    save_dir.mkdir(parents=True, exist_ok=True)

    # ---- Load images ----
    image_dir = Path(args.image_dir)
    image_paths = sorted(
        p for p in image_dir.iterdir()
        if p.suffix.lower() in (".png", ".jpg", ".jpeg", ".webp")
    )
    if not image_paths:
        raise ValueError(f"No images found in {image_dir}")

    print(f"Found {len(image_paths)} images in {image_dir}")

    # ---- Load prompts ----
    prompts_path = Path(args.prompts_file)
    with open(prompts_path, encoding="utf-8") as f:
        prompts = [line.strip() for line in f if line.strip()]

    if len(prompts) != len(image_paths):
        print(f"WARNING: {len(image_paths)} images but {len(prompts)} prompts.")
        n = min(len(image_paths), len(prompts))
        image_paths = image_paths[:n]
        prompts     = prompts[:n]
        print(f"Truncated to {n} pairs.")

    print(f"Evaluating {len(image_paths)} image-prompt pairs ...\n")

    # ---- Load images into memory ----
    images = [Image.open(p).convert("RGB") for p in image_paths]

    # ---- Initialize scorers ----
    clip_scorer = None
    vqa_scorer  = None
    dsg_scorer  = None
    t2i_scorer  = None

    if not args.skip_clip:
        clip_scorer = CLIPScorer(
            model_name=args.clip_model,
            pretrained=args.clip_pretrained,
            device=device,
        )

    if not args.skip_vqa and T2V_AVAILABLE:
        vqa_scorer = VQAScorer(model_name=args.vqa_model, device=device)
        if not args.skip_dsg:
            dsg_scorer = DSGScorer(vqa_scorer, max_claims=args.dsg_max_claims)
        if not args.skip_t2i:
            t2i_scorer = T2ICompScorer(vqa_scorer)

    # ---- Score each image ----
    t0          = time.time()
    per_image   = []

    for idx, (img, prompt, path) in enumerate(
        zip(images, prompts, image_paths)
    ):
        entry = {
            "index":    idx,
            "image":    str(path.name),
            "prompt":   prompt,
        }

        # 1. CLIPScore
        if clip_scorer is not None:
            entry["clip_score"] = clip_scorer.score(img, prompt)

        # 2. VQAScore
        if vqa_scorer is not None:
            entry["vqa_score"] = vqa_scorer.score(img, prompt)

        # 3. DSGScore
        if dsg_scorer is not None:
            dsg_mean, dsg_claims = dsg_scorer.score(img, prompt)
            entry["dsg_score"]  = dsg_mean
            entry["dsg_claims"] = dsg_claims

        # 4. T2I-CompBench scores
        if t2i_scorer is not None:
            t2i_result = t2i_scorer.score(img, prompt)
            entry["t2i_object"]    = (
                t2i_result["object"]["mean"]
                if t2i_result["object"]["mean"] is not None else None
            )
            entry["t2i_attribute"] = (
                t2i_result["attribute"]["mean"]
                if t2i_result["attribute"]["mean"] is not None else None
            )
            entry["t2i_relation"]  = (
                t2i_result["relation"]["mean"]
                if t2i_result["relation"]["mean"] is not None else None
            )
            entry["t2i_overall"]   = t2i_result["overall_mean"]
            entry["t2i_details"]   = t2i_result

        per_image.append(entry)

        # Progress
        elapsed = time.time() - t0
        eta     = elapsed / (idx + 1) * (len(images) - idx - 1)
        if (idx + 1) % 5 == 0 or idx == len(images) - 1:
            clip_str = (f"  CLIP={entry.get('clip_score', 0):.3f}"
                        if "clip_score" in entry else "")
            vqa_str  = (f"  VQA={entry.get('vqa_score', 0):.3f}"
                        if "vqa_score" in entry else "")
            dsg_str  = (f"  DSG={entry.get('dsg_score', 0):.3f}"
                        if "dsg_score" in entry else "")
            print(f"  [{idx+1:4d}/{len(images)}]{clip_str}{vqa_str}"
                  f"{dsg_str}  eta={eta/60:.1f}min  '{prompt[:45]}'")

    # ---- Aggregate statistics ----
    def agg(key):
        vals = [d[key] for d in per_image
                if key in d and d[key] is not None]
        if not vals:
            return None
        return {
            "mean": float(np.mean(vals)),
            "std":  float(np.std(vals)),
            "min":  float(np.min(vals)),
            "max":  float(np.max(vals)),
            "n":    len(vals),
        }

    aggregate = {"n_images": len(per_image)}
    for key in ["clip_score", "vqa_score", "dsg_score",
                "t2i_object", "t2i_attribute", "t2i_relation", "t2i_overall"]:
        a = agg(key)
        if a is not None:
            aggregate[key] = a

    # ---- Load baseline for comparison ----
    baseline_per_image = None
    baseline_agg       = None
    if args.baseline_json:
        try:
            with open(args.baseline_json, encoding="utf-8") as f:
                baseline_data  = json.load(f)
            baseline_per_image = baseline_data.get("per_image", [])
            baseline_agg       = baseline_data.get("aggregate", {})
            print(f"Loaded baseline from {args.baseline_json}")
        except Exception as e:
            print(f"Could not load baseline: {e}")

    # ---- Save results ----
    results = {
        "per_image": per_image,
        "aggregate": aggregate,
        "args":      vars(args),
        "eval_time_sec": time.time() - t0,
    }
    out_json = save_dir / "metrics.json"
    with open(out_json, "w", encoding="utf-8") as f:
        json.dump(results, f, indent=2, default=str)
    print(f"\nPer-image metrics saved: {out_json}")

    # ---- Summary ----
    print_summary(aggregate, baseline_agg)

    # Save summary text
    out_txt = save_dir / "summary.txt"
    with open(out_txt, "w", encoding="utf-8") as f:
        f.write("DPOK Evaluation Summary\n")
        f.write("="*60 + "\n")
        f.write(f"Image dir:     {args.image_dir}\n")
        f.write(f"Prompts file:  {args.prompts_file}\n")
        f.write(f"N images:      {len(per_image)}\n\n")
        f.write("Aggregate scores:\n")
        for key, vals in aggregate.items():
            if key == "n_images":
                continue
            if isinstance(vals, dict):
                f.write(f"  {key}: mean={vals['mean']:.4f}  "
                        f"std={vals['std']:.4f}  "
                        f"[{vals['min']:.4f}, {vals['max']:.4f}]\n")
        if baseline_agg:
            f.write("\nBaseline comparison:\n")
            for key, vals in baseline_agg.items():
                if key == "n_images" or not isinstance(vals, dict):
                    continue
                trained_mean = aggregate.get(key, {}).get("mean")
                if trained_mean is not None:
                    delta = trained_mean - vals["mean"]
                    sign  = "+" if delta >= 0 else ""
                    f.write(f"  {key}: baseline={vals['mean']:.4f}  "
                            f"trained={trained_mean:.4f}  "
                            f"delta={sign}{delta:.4f}\n")

    print(f"Summary saved: {out_txt}")

    # ---- Plots ----
    plot_save = save_dir / "score_distributions.png"
    plot_score_distributions(per_image, plot_save, baseline_per_image)

    print(f"\nAll results in: {save_dir}/")
    print("Done.")


if __name__ == "__main__":
    main()

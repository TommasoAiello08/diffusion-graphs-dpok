# 4. T2I-CompBench style scoring
# ============================================================================

def generate_t2i_questions(prompt: str) -> Dict[str, List[str]]:
    """
    Generate T2I-CompBench style sub-questions:
      - object_questions:    "Is there a [noun] in the image?"
      - attribute_questions: "Is the [noun] [adjective]?"
      - relation_questions:  "Is [A] [relation] [B]?"

    These map to the three evaluation axes in T2I-CompBench:
      attribute binding, object relationships, non-spatial relations.
    """
    text = prompt.lower().strip().rstrip(".")
    parts = re.split(r',|\bwhere\b|\bcontaining\b', text)

    obj_qs  = []
    attr_qs = []
    rel_qs  = []
    seen_nouns = []

    for part in parts:
        part = part.strip()
        stops = {"a", "an", "the", "in", "on", "at", "by", "of", "and",
                 "or", "is", "are", "with", "where", "photo", "image",
                 "scene", "shot", "containing", "showing"}

        # Extract nouns (simple heuristic: last word of 1-3 word phrases)
        phrases = re.findall(r'\b(?:a|an|the)\s+((?:\w+\s+){0,2}\w+)\b', part)
        for phrase in phrases:
            words = phrase.split()
            noun  = words[-1]
            adjs  = words[:-1]
            if noun in stops:
                continue
            if noun not in seen_nouns:
                obj_qs.append(f"Is there a {noun} in the image?")
                seen_nouns.append(noun)
            for adj in adjs:
                if adj not in stops:
                    attr_qs.append(f"Is the {noun} {adj}?")

        # Spatial / action relations
        rel_pat = re.findall(
            r'(\w+)\s+(on|near|next to|behind|in front of|above|below'
            r'|holding|riding|sitting|standing|wearing|carrying|eating)\s+'
            r'(?:a\s+|the\s+)?(\w+)',
            part,
        )
        for subj, rel, obj in rel_pat:
            if subj not in stops and obj not in stops:
                rel_qs.append(f"Is {subj} {rel} {obj}?")

    return {
        "object":    obj_qs[:4],
        "attribute": attr_qs[:4],
        "relation":  rel_qs[:4],
    }


class T2ICompScorer:
    """
    T2I-CompBench inspired scoring.
    Uses VQAScore to answer object/attribute/relation sub-questions.
    Returns per-category means and overall mean.
    """

    def __init__(self, vqa_scorer: VQAScorer):
        self.vqa = vqa_scorer

    def score(self, image: Image.Image, prompt: str) -> Dict:
        questions = generate_t2i_questions(prompt)
        results   = {}
        all_scores = []

        for category, qs in questions.items():
            if not qs:
                results[category] = {"questions": [], "mean": None}
                continue
            q_scores = []
            for q in qs:
                s = self.vqa.score(image, q)
                q_scores.append({"question": q, "score": s})
            cat_mean = float(np.mean([x["score"] for x in q_scores]))
            results[category] = {
                "questions": q_scores,
                "mean":      cat_mean,
            }
            all_scores.extend([x["score"] for x in q_scores])

        results["overall_mean"] = float(np.mean(all_scores)) if all_scores else 0.0
        return results

    def score_batch(self, images: List[Image.Image],
                    prompts: List[str]) -> List[Dict]:
        return [self.score(img, prompt)
                for img, prompt in zip(images, prompts)]



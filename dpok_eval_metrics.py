"""
DPOK Evaluation Script — Comprehensive Metrics on Pre-Generated Images

Runs four evaluation methods on a folder of images paired with their prompts:

  1. CLIPScore  — cosine similarity between image and text (open_clip ViT-L-14)
  2. VQAScore   — visual question answering similarity via CLIP-FlanT5-XXL
                  (t2v_metrics library: pip install t2v-metrics)
  3. DSGScore   — Davidsonian Scene Graph: decompose prompt into atomic claims,
                  score each with VQA, aggregate
  4. T2I-Score  — T2I-CompBench style: break prompt into object/attribute/
                  relation sub-questions, score with VQA

Designed for evaluating images already generated by run_inference.py or
dpok_eval.py.  Does NOT generate images itself.

Usage:
  # Evaluate a folder of .png/.jpg images
  python dpok_eval_metrics.py \
      --image_dir /path/to/images \
      --prompts_file /path/to/prompts.txt \
      --save_dir /path/to/results

  # Evaluate with only CLIP (skip VQA if t2v_metrics not installed)
  python dpok_eval_metrics.py \
      --image_dir /path/to/images \
      --prompts_file /path/to/prompts.txt \
      --skip_vqa

  # Compare baseline vs trained model
  python dpok_eval_metrics.py \
      --image_dir /path/to/trained/images \
      --prompts_file /path/to/prompts.txt \
      --baseline_json /path/to/baseline/metrics.json

Input format:
  --image_dir   : folder of images (img_0000.png, img_0001.png, ...)
  --prompts_file: one prompt per line, in the same order as the sorted images

Output:
  metrics.json     — per-image + aggregate scores for all methods
  summary.txt      — human-readable summary table
  score_plots.png  — distribution plots for each metric
"""


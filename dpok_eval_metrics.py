class ImageRewardScorer:
    """
    ImageReward: human preference reward model trained on image-text pairs.
    Outputs a scalar in roughly [-2, +2] (wider dynamic range than CLIPScore).

    Reference: Xu et al., "ImageReward: Learning and Evaluating Human
               Preferences for Text-to-Image Generation" (NeurIPS 2023)
    Install:   pip install image-reward
    """

    def __init__(self, model_name: str = "ImageReward-v1.0", device: str = "cuda"):
        if not IR_AVAILABLE:
            raise RuntimeError(
                "ImageReward not installed. Run: pip install image-reward"
            )
        self.device = device
        print(f"Loading ImageReward ({model_name}) ...")
        self.model = _IR.load(model_name, device=device)
        print("ImageReward scorer ready.")

    @torch.no_grad()
    def score(self, image: Image.Image, prompt: str) -> float:
        """Score a single image-prompt pair. Returns scalar (higher = better)."""
        import tempfile, os
        tmp = tempfile.NamedTemporaryFile(suffix=".png", delete=False)
        image.convert("RGB").save(tmp.name)
        tmp.close()
        try:
            s = self.model.score(prompt, tmp.name)
        finally:
            os.unlink(tmp.name)
        return float(s)

    def score_batch(self, images: List[Image.Image],
                    prompts: List[str]) -> List[float]:
        return [self.score(img, pr) for img, pr in zip(images, prompts)]



class CLIPScorer:
    """
    Standard CLIP image-text cosine similarity.
    Uses open_clip — same library as dpok_scene_reward.py.
    """

    def __init__(self, model_name: str = "ViT-L-14",
                 pretrained: str = "openai", device: str = "cuda"):
        self.device = device
        print(f"Loading CLIP ({model_name}/{pretrained}) for CLIPScore ...")
        self.model, _, self.preprocess = open_clip.create_model_and_transforms(
            model_name, pretrained=pretrained,
        )
        self.model     = self.model.to(device).eval()
        self.tokenizer = open_clip.get_tokenizer(model_name)
        for p in self.model.parameters():
            p.requires_grad_(False)
        print("CLIP scorer ready.")

    @torch.no_grad()
    def score(self, image: Image.Image, prompt: str) -> float:
        img_t   = self.preprocess(image).unsqueeze(0).to(self.device)
        img_f   = F.normalize(self.model.encode_image(img_t), dim=-1)
        tok     = self.tokenizer([prompt]).to(self.device)
        txt_f   = F.normalize(self.model.encode_text(tok), dim=-1)
        return float((img_f * txt_f).sum().cpu())

    @torch.no_grad()
    def score_batch(self, images: List[Image.Image],
                    prompts: List[str]) -> List[float]:
        scores = []
        for img, prompt in zip(images, prompts):
            scores.append(self.score(img, prompt))
        return scores



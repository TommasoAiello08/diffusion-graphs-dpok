class VQAScorer:
    """
    VQAScore using CLIP-FlanT5-XXL from t2v_metrics.
    Answers "Does this image show [prompt]?" with a probability.

    Reference: Lin et al., "EvalCrafter" / "VQAScore" (2024)
    Install:   pip install t2v-metrics
    """

    def __init__(self, model_name: str = "clip-flant5-xl",
                 device: str = "cuda"):
        if not T2V_AVAILABLE:
            raise RuntimeError("t2v_metrics not installed. "
                               "Run: pip install t2v-metrics")
        self.device = device
        print(f"Loading VQA model ({model_name}) ...")
        self.scorer = t2v_metrics.VQAScore(model=model_name, device=device)
        print("VQA scorer ready.")

    def score(self, image, prompt: str) -> float:
        """Score a single image-prompt pair.
        image: PIL.Image or file path string — t2v_metrics requires a file path,
               so PIL images are saved to a temp file automatically.
        """
        import tempfile, os
        if hasattr(image, "save"):   # PIL Image — save to tmp file
            tmp = tempfile.NamedTemporaryFile(suffix=".png", delete=False)
            image.save(tmp.name)
            tmp.close()
            try:
                result = self.scorer(images=[tmp.name], texts=[prompt])
            finally:
                os.unlink(tmp.name)
        else:                        # already a file path string
            result = self.scorer(images=[str(image)], texts=[prompt])
        return float(result[0, 0].cpu())

    def score_batch(self, images, prompts: List[str]) -> List[float]:
        """Score multiple image-prompt pairs (one prompt per image)."""
        return [self.score(img, prompt) for img, prompt in zip(images, prompts)]



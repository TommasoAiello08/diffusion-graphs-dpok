# 3. DSGScore — Davidsonian Scene Graph decomposition
# ============================================================================

def extract_atomic_claims(prompt: str, max_claims: int = 8) -> List[str]:
    """
    Extract atomic yes/no claims from a prompt using simple rule-based
    decomposition.  For a full DSG, you would use an LLM (GPT-4 or similar).
    This lightweight version extracts:
      - Objects mentioned ("Is there a [noun]?")
      - Attributes ("Is the [noun] [adj]?")
      - Spatial/action relations ("Is [noun] [verb/prep] [noun]?")

    Example:
      "a red car next to a blue bicycle"
      → ["Is there a car?", "Is the car red?",
         "Is there a bicycle?", "Is the bicycle blue?",
         "Is the car next to a bicycle?"]
    """
    claims = []
    text   = prompt.lower().strip().rstrip(".")

    # Pattern: "a/an <adj>* <noun>"
    noun_adj_pat = re.findall(
        r'\b(?:a|an|the)\s+((?:\w+\s+){0,3}?\w+)\b', text
    )

    # Simple heuristic: split on commas and "where"/"containing"
    parts = re.split(r',|\bwhere\b|\bcontaining\b', text)

    seen_nouns = []
    for part in parts:
        part = part.strip()
        if not part:
            continue
        # Find adjective-noun pairs: "red car", "blue bicycle"
        adj_noun = re.findall(r'\b([a-z]+)\s+([a-z]+)\b', part)
        for adj, noun in adj_noun:
            # Skip stopwords
            stops = {"a", "an", "the", "in", "on", "at", "by", "of",
                     "and", "or", "is", "are", "with", "where", "photo",
                     "image", "scene", "shot", "view", "containing"}
            if adj in stops or noun in stops:
                continue
            # Object claim
            if noun not in seen_nouns:
                claims.append(f"Is there a {noun} in the image?")
                seen_nouns.append(noun)
            # Attribute claim
            claims.append(f"Is the {noun} {adj}?")

        # Relation claims: "X [prep] Y" or "X [verb] Y"
        rel_pat = re.findall(
            r'\b(\w+)\s+(on|near|next to|behind|in front of|above|below'
            r'|holding|riding|sitting on|standing on|wearing)\s+(?:a\s+)?(\w+)',
            part,
        )
        for subj, rel, obj in rel_pat:
            if subj not in {"a", "an", "the"} and obj not in {"a", "an", "the"}:
                claims.append(f"Is {subj} {rel} {obj}?")

    # Fallback: just ask about the full prompt
    if not claims:
        claims.append(f"Does the image show: {prompt}?")

    return claims[:max_claims]


class DSGScorer:
    """
    Davidsonian Scene Graph evaluation.
    Decomposes each prompt into atomic claims, scores each with VQA,
    then returns the mean score (= how many claims the image satisfies).

    Full DSG (from Lee et al., 2023) uses GPT-4 for claim extraction.
    This uses rule-based extraction for reproducibility without an API key.
    """

    def __init__(self, vqa_scorer: VQAScorer, max_claims: int = 8):
        self.vqa        = vqa_scorer
        self.max_claims = max_claims

    def score(self, image: Image.Image, prompt: str
              ) -> Tuple[float, List[Dict]]:
        """
        Returns (mean_score, [{"claim": str, "score": float}, ...])
        """
        claims = extract_atomic_claims(prompt, self.max_claims)
        if not claims:
            return 0.0, []
        claim_scores = []
        for claim in claims:
            s = self.vqa.score(image, claim)
            claim_scores.append({"claim": claim, "score": s})
        mean_s = float(np.mean([c["score"] for c in claim_scores]))
        return mean_s, claim_scores

    def score_batch(self, images: List[Image.Image], prompts: List[str]
                    ) -> Tuple[List[float], List[List[Dict]]]:
        all_scores = []
        all_claims = []
        for img, prompt in zip(images, prompts):
            s, cs = self.score(img, prompt)
            all_scores.append(s)
            all_claims.append(cs)
        return all_scores, all_claims


